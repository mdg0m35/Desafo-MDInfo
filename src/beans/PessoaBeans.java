package beans;

/**
 * Modelo Pessoa
 */
public class PessoaBeans {

	// Atributos
	private int idPessoa, etapa1, etapa2;
	private String nomePessoa, sobrenomePessoa;
	public int setIdPessoa;

	// Get e Set
	public int getIdPessoa() {
		return idPessoa;
	}

	public void setIdPessoa(int idPessoa) {
		this.idPessoa = idPessoa;
	}

	public String getNomePessoa() {
		return nomePessoa;
	}

	public void setNomePessoa(String nomePessoa) {
		this.nomePessoa = nomePessoa;
	}

	public String getSobrenomePessoa() {
		return sobrenomePessoa;
	}

	public void setSobrenomePessoa(String sobrenomePessoa) {
		this.sobrenomePessoa = sobrenomePessoa;
	}

	public int getEtapa1() {
		return etapa1;
	}

	public void setEtapa1(int etapa1) {
		this.etapa1 = etapa1;
	}

	public int getEtapa2() {
		return etapa2;
	}

	public void setEtapa2(int etapa2) {
		this.etapa2 = etapa2;
	}

	public int getSetIdPessoa() {
		return setIdPessoa;
	}

	public void setSetIdPessoa(int setIdPessoa) {
		this.setIdPessoa = setIdPessoa;
	}

}
